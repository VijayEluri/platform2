package com.idega.data;

/**
 * @author gimmi
 */
public class NoIndexException extends Exception {

	public NoIndexException() {
		super();
	}

	public NoIndexException(String arg0) {
		super(arg0);
	}

	public NoIndexException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public NoIndexException(Throwable arg0) {
		super(arg0);
	}

}
